#include "cops.h"

/**
 * add - adds two numbers
 * @a: int
 * @b: int
 *
 * Return: sum(a + b)
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two numbers
 * @a: int
 * @b: int
 *
 * Return: sub(a - b)
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two numbers
 * @a: int
 * @b: int
 *
 * Return: mul(a * b)
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two numbers
 * @a: int a;
 * @b: int b;
 *
 * Return: div(a / b)
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - calculates a mod b
 * @a: int a
 * @b: int b
 *
 * Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}
